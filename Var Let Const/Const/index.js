//Declaration
const a;
console.log(a);

//Initialization
const b;
b=20;
console.log(b);

//Declaration and Initialization at the same time
const c=10;           
console.log(c);

//Re-Initialization
const d;
d=10;
d=20;
console.log(d);

//Re-Declaration and Re-Initialization at the same time
const e="Egg";
const e="Hen";
console.log(e);

//Hoisting
console.log(f);
const f="Fish";
